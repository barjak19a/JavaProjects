package z11;

public class Kap {
	private double  x, y;
	private double q;
	
	public Kap(double x, double y, double q) {
		super();
		this.x = x;
		this.y = y;
		this.q = q;
	}
	
	public double getX() {
		return x;
	}
	public double getY() {
		return y;
	}
	public double getQ() {
		return q;
	}
	
}
